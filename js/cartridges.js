//Hides and Unhides the Platinum Serial Code section
function hidePlatSerial(){
    if (document.getElementById("platSerial").style.display === "none") {
        document.getElementById("platSerial").style.display = "block";
    } else {
        document.getElementById("platSerial").style.display = "none";
    }
}

